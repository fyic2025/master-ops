const t = require('./tmp-tasks.json');
const auto = t.tasks.filter(x => x.execution_type === 'auto' && x.status !== 'completed');
console.log('Non-completed auto tasks:', auto.length);
console.log('');
auto.forEach(x => console.log(`${x.status.padEnd(20)} | ${x.id}: ${x.title}`));
console.log('');
console.log('By status:');
const byStatus = {};
auto.forEach(x => { byStatus[x.status] = (byStatus[x.status] || 0) + 1; });
Object.entries(byStatus).forEach(([s,c]) => console.log(`  ${s}: ${c}`));
